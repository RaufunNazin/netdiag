import { useEffect, useState } from "react";

const ThemeToggleFab = ({ colorMode, onToggle, className = "" }) => {
  const [isDark, setIsDark] = useState(colorMode === "dark");

  useEffect(() => {
    setIsDark(colorMode === "dark");
  }, [colorMode]);

  useEffect(() => {
    const handleKeyDown = (event) => {
      const activeTag = document.activeElement.tagName;
      const isInputActive = ["INPUT", "TEXTAREA", "SELECT"].includes(activeTag);

      if (isInputActive) return;

      if (event.key === "M" && event.shiftKey) {
        onToggle((prev) => (prev === "dark" ? "light" : "dark"));
      }
    };

    window.addEventListener("keydown", handleKeyDown);
    return () => window.removeEventListener("keydown", handleKeyDown);
  }, [onToggle]);

  return (
    <button
      onClick={() => onToggle((prev) => (prev === "dark" ? "light" : "dark"))}
      className={`
        theme-fab-btn relative z-10 p-3 rounded-full transition-all duration-200 ease-in-out flex items-center justify-center
        ${
          isDark
            ? "bg-neutral-800 text-yellow-400 hover:bg-neutral-700 hover:text-yellow-300"
            : "bg-neutral-300 hover:bg-neutral-400 text-neutral-700 hover:text-neutral-900"
        } 
        ${className}
      `}
      title={`Switch to ${isDark ? "Light" : "Dark"} Mode [Shift + M]`}
    >
      <div className="relative w-4 h-4 overflow-hidden">
        <svg
          xmlns="http://www.w3.org/2000/svg"
          fill="none"
          viewBox="0 0 24 24"
          strokeWidth={2}
          stroke="currentColor"
          className={`absolute inset-0 w-full h-full transform transition-all duration-500 ease-spring ${
            isDark ? "rotate-0 opacity-100" : "rotate-90 opacity-0"
          }`}
        >
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            d="M12 3v2.25m6.364.386l-1.591 1.591M21 12h-2.25m-.386 6.364l-1.591-1.591M12 18.75V21m-4.773-4.227l-1.591 1.591M5.25 12H3m4.227-4.773L5.636 5.636M15.75 12a3.75 3.75 0 11-7.5 0 3.75 3.75 0 017.5 0z"
          />
        </svg>

        <svg
          xmlns="http://www.w3.org/2000/svg"
          fill="none"
          viewBox="0 0 24 24"
          strokeWidth={2}
          stroke="currentColor"
          className={`absolute inset-0 w-full h-full transform transition-all duration-500 ease-spring ${
            isDark ? "-rotate-90 opacity-0" : "rotate-0 opacity-100"
          }`}
        >
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            d="M21.752 15.002A9.718 9.718 0 0118 15.75c-5.385 0-9.75-4.365-9.75-9.75 0-1.33.266-2.597.748-3.752A9.753 9.753 0 003 11.25C3 16.635 7.365 21 12.75 21a9.753 9.753 0 009.002-5.998z"
          />
        </svg>
      </div>
    </button>
  );
};

export default ThemeToggleFab;
